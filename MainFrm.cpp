void CMainFrame::OnClose() 
{
	// Save off the size and position of this frame window
	// ------------------------------------------------------------------------

	CDitherStudioApp * pApp = static_cast<CDitherStudioApp *>( AfxGetApp() );
	
	if ( pApp ) {

		pApp->StoreFrameWindowSettings( this, "MainFrameWnd" );

	} 

	CMDIFrameWnd::OnClose();
}

